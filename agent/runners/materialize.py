"""Materialize Agent runtime configuration without editing user config files."""

from __future__ import annotations

import re
import shlex
from pathlib import Path
from typing import Any


_ENV_NAME_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*$")


def build_runtime_env(plan: dict[str, Any]) -> dict[str, str]:
    """Return the environment that should be used for one Agent job."""
    env: dict[str, str] = {}
    for source in (
        plan.get("execution", {}).get("environment", {}),
        plan.get("materialized_config", {}),
    ):
        if not isinstance(source, dict):
            continue
        for key, value in source.items():
            if not _ENV_NAME_RE.match(str(key)):
                continue
            if value is None:
                continue
            env[str(key)] = str(value)
    return env


def materialize_runtime_env(plan: dict[str, Any], run_dir: str | Path) -> str:
    """Write a sourceable runtime env file and return its path."""
    path = Path(run_dir) / "runtime.env"
    env = build_runtime_env(plan)
    lines = [
        "# Generated by benchmark Agent for this job only.",
        "# Do not edit config/user_config.sh for Agent-generated runtime values.",
    ]
    for key in sorted(env):
        lines.append(f"export {key}={_shell_quote(env[key])}")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return str(path)


def load_runtime_env_file(path: str | Path) -> dict[str, str]:
    """Load a runtime env file generated by materialize_runtime_env."""
    env: dict[str, str] = {}
    for raw_line in Path(path).read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#"):
            continue
        if line.startswith("export "):
            line = line[len("export ") :]
        if "=" not in line:
            continue
        key, raw_value = line.split("=", 1)
        key = key.strip()
        if not _ENV_NAME_RE.match(key):
            continue
        try:
            values = shlex.split(raw_value, posix=True)
        except ValueError:
            values = [raw_value]
        env[key] = values[0] if values else ""
    return env


def _shell_quote(value: str) -> str:
    return "'" + value.replace("'", "'\"'\"'") + "'"
